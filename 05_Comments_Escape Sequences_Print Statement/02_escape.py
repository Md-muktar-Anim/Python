""" Escape Sequences:
Escape sequences are used to include special characters in strings.
Common escape sequences:
\n : Newline
\t : Tab
\\ : Backslash
\" : Double quote
\' : Single quote """

print("Hey how are you \nI am good\\newline")
print("Hello \" World")
print("Hello \' World")
print("This is a tab\tcharacter")